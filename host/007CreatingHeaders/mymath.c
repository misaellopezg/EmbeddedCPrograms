int fctn_add(int a, int b)
{
	int result = a+b;
	return result;
}

int fctn_sub(int a, int b)
{
	int result = a-b;
	return result;
}

int fctn_mult(int a, int b)
{
	int result = a*b;
	return result;
}

int fctn_div(int a, int b)
{
	int result = a/b;
	return result;
}

